

-- Snowflake CREATE TABLE for FUND_TARGETS
CREATE TABLE FUND_TARGETS (
    FUND_ID NUMBER NOT NULL,
    ASSET_OBJECTIVE_ID NUMBER NOT NULL,
    ENGAGEMENT_TYPE_ID NUMBER NOT NULL,
    FREQUENCY_ID NUMBER NOT NULL,
    TARGET_DESCRIPTION STRING(500),
    PRIMARY KEY (FUND_ID, ASSET_OBJECTIVE_ID, ENGAGEMENT_TYPE_ID, FREQUENCY_ID),
    FOREIGN KEY (FUND_ID) REFERENCES POTENTIAL_FUNDS(FUND_ID),
    FOREIGN KEY (ASSET_OBJECTIVE_ID) REFERENCES ASSET_OBJECTIVES(ASSET_OBJECTIVE_ID),
    FOREIGN KEY (ENGAGEMENT_TYPE_ID) REFERENCES ENGAGEMENT_TYPES(ENGAGEMENT_TYPE_ID),
    FOREIGN KEY (FREQUENCY_ID) REFERENCES ENGAGEMENT_FREQUENCIES(FREQUENCY_ID)
);



