

-- Snowflake CREATE TABLE for FUND_ASSETS
CREATE TABLE FUND_ASSETS (
    FUND_ID NUMBER NOT NULL,
    ASSET_CLASS_ID NUMBER NOT NULL,
    PERCENT_OF_FUND NUMBER,
    PRIMARY KEY (FUND_ID, ASSET_CLASS_ID),
    FOREIGN KEY (FUND_ID) REFERENCES POTENTIAL_FUNDS(FUND_ID),
    FOREIGN KEY (ASSET_CLASS_ID) REFERENCES ASSET_CLASSES(ASSET_CLASS_ID)
);


  
