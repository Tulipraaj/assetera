

-- Snowflake CREATE TABLE for CUSTOMERS
CREATE TABLE CUSTOMERS (
    CUSTOMER_ID NUMBER NOT NULL,
    CONTACT_LAST_NAME STRING(26),
    CONTACT_FIRST_NAME STRING(26),
    STREET STRING(128),
    CITY STRING(26),
    STATE STRING(26),
    ZIP NUMBER,
    COUNTRY STRING(26),
    AGE NUMBER,
    MARITAL_STATUS STRING(26),
    GENDER STRING(26),
    NUMBER_OF_DEPENDENTS NUMBER,
    PRIMARY KEY (CUSTOMER_ID)
);



